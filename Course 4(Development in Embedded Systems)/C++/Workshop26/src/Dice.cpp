int BUTTON = 7;
int rollMode = 1;

void setup() {
    pinMode(BUTTON, INPUT_PULLUP);
    matrix.begin();
    Serial.begin(9600);
}

int i = 0;

void loop() {
    if(rollMode) {
        matrix.renderBitmap(num[i], 8, 12);
        if(digitalRead(BUTTON) != HIGH) {
            rollMode = 0;
        }
        Serial.println(i);
        delay(10);
        i++;
        if(i == 6) i = 0;
    }
    else {
        if(digitalRead(BUTTON) != HIGH) {
            rollMode = 1;
        }
        delay(10);
    }
}